import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repositoryRoot = resolve(dirname(fileURLToPath(import.meta.url)), '../..');
const sourcePath = join(repositoryRoot, 'docs/low-code-refactor/shared-designer-contract.json');
const source = JSON.parse(readFileSync(sourcePath, 'utf8'));
const typescriptPath = join(repositoryRoot, source.generatedArtifacts.typescript);
const csharpPath = join(repositoryRoot, source.generatedArtifacts.csharp);
const checkOnly = process.argv.includes('--check');

const typescript = renderTypeScript(source);
const csharp = renderCSharp(source);

writeOrCheck(typescriptPath, typescript);
writeOrCheck(csharpPath, csharp);

function renderTypeScript(contract) {
  const document = contract.document;
  return `// <auto-generated />\n// Source: docs/low-code-refactor/shared-designer-contract.json\n\nexport const GENERATED_DESIGNER_CONTRACT = ${JSON.stringify({
    schemaVersion: contract.schemaVersion,
    document,
    actions: contract.actions,
    bindings: contract.bindings
  }, null, 2)} as const;\n`;
}

function renderCSharp(contract) {
  const document = contract.document;
  const lines = [
    '// <auto-generated />',
    '// Source: docs/low-code-refactor/shared-designer-contract.json',
    '',
    'namespace AsterERP.Contracts.ApplicationDevelopmentCenter;',
    '',
    'public static class GeneratedDesignerContract',
    '{',
    `    public const int SchemaVersion = ${contract.schemaVersion};`,
    ...renderSet('DraftRequiredFields', document.draftRequiredFields),
    ...renderSet('RuntimeRequiredFields', document.runtimeRequiredFields),
    ...renderSet('RuntimeArrayFields', document.runtimeArrayFields),
    ...renderSet('RuntimeObjectFields', document.runtimeObjectFields),
    ...renderSet('EditorOnlyFields', document.editorOnlyFields),
    ...renderSet('ForbiddenKeys', document.forbiddenKeys),
    ...renderSet('ActionStepTypes', contract.actions.stepTypes),
    ...renderSet('ActionTriggers', contract.actions.triggers),
    ...renderSet('VariableSources', contract.bindings.variableSources),
    ...renderSet('ValueTypes', contract.bindings.valueTypes),
    '}'
  ];

  return `${lines.join('\n')}\n`;
}

function renderSet(name, values) {
  const literalValues = values.map((value) => `"${escapeCSharp(value)}"`).join(', ');
  return [
    `    public static readonly IReadOnlySet<string> ${name} =`,
    `        new HashSet<string>(new[] { ${literalValues} }, StringComparer.Ordinal);`
  ];
}

function escapeCSharp(value) {
  return value.replaceAll('\\', '\\\\').replaceAll('"', '\\"');
}

function writeOrCheck(path, content) {
  let current = null;
  try {
    current = readFileSync(path, 'utf8');
  } catch (error) {
    if (!checkOnly && error.code !== 'ENOENT') throw error;
  }

  if (checkOnly) {
    if (current !== content) throw new Error(`Generated contract is stale: ${path}`);
    return;
  }

  writeFileSync(path, content, 'utf8');
}
